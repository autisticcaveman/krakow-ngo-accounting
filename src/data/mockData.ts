// ZUS rates 2026
export const ZUS_RATES = {
  // Employee (from gross)
  emerytalne_pracownik: 0.0976,
  rentowe_pracownik: 0.015,
  chorobowe: 0.0245,
  zdrowotne: 0.09,
  // Employer (additional cost)
  emerytalne_pracodawca: 0.0976,
  rentowe_pracodawca: 0.065,
  wypadkowe: 0.0167,
  fp: 0.0245,
  fgsp: 0.001,
};

export const PIT_RATES = {
  threshold: 120000,
  rate1: 0.12,
  rate2: 0.32,
  monthlyRelief: 300, // ulga podatkowa
};

export function calculatePayslip(employee: Employee, month: string): Payslip {
  const gross = employee.grossSalary;

  const zus = {
    emerytalne_pracownik: gross * ZUS_RATES.emerytalne_pracownik,
    rentowe_pracownik: gross * ZUS_RATES.rentowe_pracownik,
    chorobowe_pracownik: gross * ZUS_RATES.chorobowe,
    zdrowotne: (gross - gross * (ZUS_RATES.emerytalne_pracownik + ZUS_RATES.rentowe_pracownik + ZUS_RATES.chorobowe)) * ZUS_RATES.zdrowotne,
    emerytalne_pracodawca: gross * ZUS_RATES.emerytalne_pracodawca,
    rentowe_pracodawca: gross * ZUS_RATES.rentowe_pracodawca,
    wypadkowe: gross * ZUS_RATES.wypadkowe,
    fp: gross * ZUS_RATES.fp,
    fgsp: gross * ZUS_RATES.fgsp,
  };

  const totalZusPracownik = zus.emerytalne_pracownik + zus.rentowe_pracownik + zus.chorobowe_pracownik;
  const taxBase = Math.round(gross - totalZusPracownik - 250); // 250 PLN koszty uzyskania
  const pitBeforeRelief = Math.round(taxBase * PIT_RATES.rate1);
  const pitAdvance = Math.max(0, pitBeforeRelief - PIT_RATES.monthlyRelief);
  const netSalary = gross - totalZusPracownik - zus.zdrowotne - pitAdvance;

  const totalEmployerCost = gross + zus.emerytalne_pracodawca + zus.rentowe_pracodawca + zus.wypadkowe + zus.fp + zus.fgsp;

  return {
    id: `ps_${employee.id}_${month}`,
    employeeId: employee.id,
    employee,
    month,
    grossSalary: gross,
    zus,
    taxBase,
    pitAdvance,
    taxRelief: PIT_RATES.monthlyRelief,
    netSalary: Math.round(netSalary * 100) / 100,
    totalEmployerCost: Math.round(totalEmployerCost * 100) / 100,
    generatedAt: new Date().toISOString(),
  };
}

